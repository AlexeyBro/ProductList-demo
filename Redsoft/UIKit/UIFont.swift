import UIKit

extension UIFont {
    static func default14() -> UIFont? {
        return UIFont.systemFont(ofSize: 14)
    }
    
    static func defaultBold16() -> UIFont? {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    
    static func defaultBold20() -> UIFont? {
        return UIFont.boldSystemFont(ofSize: 20)
    }
}
